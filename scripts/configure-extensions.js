#!/usr/bin/env node
// Build-time extension configuration for react-native-duckdb.
// Reads extensions from consumer's package.json and generates
// duckdb/extension/extension_config_local.cmake.

const fs = require('fs');
const path = require('path');

const IN_TREE_EXTENSIONS = [
  'core_functions',
  'parquet',
  'json',
  'icu',
  'autocomplete',
  'tpch',
  'tpcds',
  'delta',
];

const OUT_OF_TREE_EXTENSIONS = {
  sqlite_scanner: {
    git_url: 'https://github.com/duckdb/duckdb-sqlite',
    git_tag: '0c93d610af1e1f66292559fcf0f01a93597a98b6',
  },
};

const ALL_VALID = [...IN_TREE_EXTENSIONS, ...Object.keys(OUT_OF_TREE_EXTENSIONS)];

// DuckDB's extension_config.cmake loads these by default
const DEFAULT_EXTENSIONS = ['core_functions', 'parquet'];

function checkPackageJson(pkgPath) {
  if (!fs.existsSync(pkgPath)) return null;
  try {
    const pkg = JSON.parse(fs.readFileSync(pkgPath, 'utf8'));
    const deps = {
      ...(pkg.dependencies || {}),
      ...(pkg.devDependencies || {}),
    };
    if (deps['react-native-duckdb']) {
      return { path: pkgPath, pkg };
    }
  } catch (_) {
    // skip malformed package.json
  }
  return null;
}

function findConsumerPackageJson(startDir) {
  let dir = startDir;
  const root = path.parse(dir).root;

  while (dir !== root) {
    // Check this directory
    const result = checkPackageJson(path.join(dir, 'package.json'));
    if (result) return result;

    // Check workspace siblings: if dir has a package.json with workspaces,
    // search each workspace for the consumer package.json
    const parentPkgPath = path.join(dir, 'package.json');
    if (fs.existsSync(parentPkgPath)) {
      try {
        const parentPkg = JSON.parse(fs.readFileSync(parentPkgPath, 'utf8'));
        const workspaces = parentPkg.workspaces || [];
        for (const ws of workspaces) {
          // Handle simple workspace entries (not globs)
          const wsDir = path.join(dir, ws);
          const wsResult = checkPackageJson(path.join(wsDir, 'package.json'));
          if (wsResult) return wsResult;
        }
      } catch (_) {
        // skip
      }
    }

    dir = path.dirname(dir);
  }
  return null;
}

function parseArgs() {
  const args = process.argv.slice(2);
  const opts = { duckdbPath: null, appRoot: null };

  for (let i = 0; i < args.length; i++) {
    if (args[i] === '--duckdb-path' && args[i + 1]) {
      opts.duckdbPath = args[i + 1];
      i++;
    } else if (args[i] === '--app-root' && args[i + 1]) {
      opts.appRoot = args[i + 1];
      i++;
    } else if (args[i] === '--help') {
      console.log('Usage: configure-extensions.js [--duckdb-path <path>] [--app-root <path>]');
      console.log('');
      console.log('Reads react-native-duckdb.build.extensions from consumer package.json');
      console.log('and generates duckdb/extension/extension_config_local.cmake.');
      console.log('');
      console.log('--app-root  Directory to start searching for consumer package.json');
      console.log('            (defaults to process.cwd())');
      console.log(`Valid extensions: ${ALL_VALID.join(', ')}`);
      process.exit(0);
    }
  }

  return opts;
}

function generateCmake(extensions, duckdbPath) {
  const lines = [
    '# Auto-generated by react-native-duckdb configure-extensions.js',
    '# DO NOT EDIT — this file is regenerated during each build.',
    `# Extensions: ${extensions.length > 0 ? extensions.join(', ') : '(none)'}`,
    '',
  ];

  // Determine which default extensions need to be skipped
  const skipList = DEFAULT_EXTENSIONS.filter((ext) => !extensions.includes(ext));
  if (skipList.length > 0) {
    lines.push('# Skip default extensions not requested by user');
    lines.push(`set(SKIP_EXTENSIONS "${skipList.join(';')}")`);
    lines.push('');
  }

  if (extensions.length > 0) {
    lines.push('# Load user-configured extensions');
    for (const ext of extensions) {
      if (OUT_OF_TREE_EXTENSIONS[ext]) {
        const oot = OUT_OF_TREE_EXTENSIONS[ext];
        lines.push(
          `duckdb_extension_load(${ext} GIT_URL ${oot.git_url} GIT_TAG ${oot.git_tag})`
        );
      } else {
        lines.push(`duckdb_extension_load(${ext})`);
      }
    }
  }

  lines.push('');
  return lines.join('\n');
}

function main() {
  const opts = parseArgs();

  // Resolve duckdb path
  const scriptDir = __dirname;
  const duckdbPath = opts.duckdbPath
    ? path.resolve(opts.duckdbPath)
    : path.resolve(scriptDir, '..', 'duckdb');

  if (!fs.existsSync(path.join(duckdbPath, 'CMakeLists.txt'))) {
    console.error(`❌ react-native-duckdb: DuckDB source not found at ${duckdbPath}`);
    console.error('   Run: git submodule update --init');
    process.exit(1);
  }

  // Find consumer package.json (start from --app-root or cwd)
  const searchStart = opts.appRoot ? path.resolve(opts.appRoot) : process.cwd();
  const consumer = findConsumerPackageJson(searchStart);
  let extensions = [];

  if (consumer) {
    const config = consumer.pkg['react-native-duckdb'];
    if (config && config.build && Array.isArray(config.build.extensions)) {
      extensions = config.build.extensions;
    }
  }

  // Warn on empty extensions
  if (extensions.length === 0) {
    console.warn(
      '⚠️  react-native-duckdb: No extensions configured. Common SQL functions like ' +
        'sum(), avg(), and list_value() will be unavailable. Add extensions in your ' +
        'package.json under "react-native-duckdb".build.extensions'
    );
  }

  // Validate extension names
  const invalid = extensions.filter((name) => !ALL_VALID.includes(name));
  if (invalid.length > 0) {
    for (const name of invalid) {
      console.error(
        `❌ react-native-duckdb: Unknown extension '${name}'. Valid extensions: ${ALL_VALID.join(', ')}`
      );
    }
    process.exit(1);
  }

  // Generate cmake config
  const cmakeContent = generateCmake(extensions, duckdbPath);
  const outputPath = path.join(duckdbPath, 'extension', 'extension_config_local.cmake');
  fs.writeFileSync(outputPath, cmakeContent);
  console.log(`✅ react-native-duckdb: Generated ${outputPath}`);
  console.log(`   Extensions: ${extensions.length > 0 ? extensions.join(', ') : '(none — defaults skipped)'}`);

  // Write marker file for build cache
  const markerPath = path.join(duckdbPath, '.extensions-configured');
  fs.writeFileSync(markerPath, JSON.stringify({ extensions, timestamp: new Date().toISOString() }));
}

main();
